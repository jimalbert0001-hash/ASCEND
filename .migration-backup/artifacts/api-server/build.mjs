async function buildAll() {
  const distDir = path.resolve(artifactDir, "dist");
  await rm(distDir, { recursive: true, force: true });

  // ── 1. Replit dev-server bundle (src/index.ts → dist/index.mjs) ─────────────
  await esbuild({
    entryPoints: [path.resolve(artifactDir, "src/index.ts")],
    platform: "node",
    bundle: true,
    format: "esm",
    outdir: distDir,
    outExtension: { ".js": ".mjs" },
    logLevel: "info",
    external: [
      ...UNBUNDLEABLE,
      "ws",
      "passport",
      "express-session",
      "connect-pg-simple",
    ],
    sourcemap: "linked",
    plugins: [
      // pino relies on workers to handle logging; the plugin handles bundling them
      esbuildPluginPino({ transports: ["pino-pretty"] }),
    ],
    banner: { js: ESM_BANNER },
  });

  // ── 2. Vercel serverless function (src/app.ts → ../../api/index.mjs) ─────────
  // Bundles workspace packages inline; externalises only native/unbundleable deps.
  // logger.ts already omits pino-pretty in production, so no worker threads needed.
  await esbuild({
    entryPoints: [path.resolve(artifactDir, "src/app.ts")],
    platform: "node",
    bundle: true,
    format: "esm",
    outfile: path.resolve(artifactDir, "../../api/index.mjs"),
    logLevel: "info",
    external: UNBUNDLEABLE,
    sourcemap: false,
    plugins: [],
    banner: { js: ESM_BANNER },
  });
}

buildAll().catch((err) => {
  console.error(err);
  process.exit(1);
});
